import { BlogModel } from '../../domain/blogs.model';

export const ConvertBlogToViewModel = (blog: BlogModel) => {
  return {
    id: blog.id,
    name: blog.name,
    description: blog.description,
    websiteUrl: blog.websiteUrl,
    createdAt: blog.createdAt,
    isMembership: blog.isMembership,
  };
};
